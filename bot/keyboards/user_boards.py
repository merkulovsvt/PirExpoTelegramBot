# Reply –∫–ª–∞–≤–∏–∞—Ç—É—Ä–∞ –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏ –Ω–æ–º–µ—Ä–∞ —Ç–µ–ª–µ—Ñ–æ–Ω–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º +
def reply_get_phone_number() -> (str, ReplyKeyboardBuilder):
    builder = ReplyKeyboardBuilder()
    builder.add(KeyboardButton(text=' üìû –ü–æ–¥–µ–ª–∏—Ç—å—Å—è –Ω–æ–º–µ—Ä–æ–º —Ç–µ–ª–µ—Ñ–æ–Ω', request_contact=True))

    text = "–ó–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä—É–π—Ç–µ—Å—å, –ø–æ–¥–µ–ª–∏–≤—à–∏—Å—å —Å –Ω–∞–º–∏ –≤–∞—à–∏–º –Ω–æ–º–µ—Ä–æ–º —Ç–µ–ª–µ—Ñ–æ–Ω–∞:"
    return text, builder.as_markup(resize_keyboard=True)


# Reply –∫–ª–∞–≤–∏–∞—Ç—É—Ä–∞ –¥–ª—è –æ—Å–Ω–æ–≤–Ω–æ–≥–æ –º–µ–Ω—é +
def reply_main_menu(phone=None) -> (str, ReplyKeyboardBuilder):
    builder = ReplyKeyboardBuilder()
    buttons = ("üõí –ó–∞–∫–∞–∑—ã", "üé´ –ë–∏–ª–µ—Ç—ã", "üìÖ –†–∞—Å–ø–∏—Å–∞–Ω–∏–µ", "üéâ –ú–µ—Ä–æ–ø—Ä–∏—è—Ç–∏—è", "ü§ù –≠–∫—Å–ø–æ–Ω–µ–Ω—Ç—ã", "ü§ù –ü–∞—Ä—Ç–Ω—ë—Ä—ã")

    for elem in buttons:
        builder.add(KeyboardButton(text=elem))
    builder.adjust(2, 2, 2)

    start_text = ('–î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –≤ –Ω–∞—à–µ–≥–æ –±–æ—Ç–∞!\n–ú—ã - –∫–æ–º–ø–∞–Ω–∏—è –ü–∏—Ä –≠–∫—Å–ø–æ, –∑–∞–Ω–∏–º–∞–µ–º—Å—è –æ—Ä–≥–∞–Ω–∏–∑–∞—Ü–∏–µ–π –≤—ã—Å—Ç–∞–≤–æ–∫ –≤ —Å—Ñ–µ—Ä–µ '
                  '–≥–æ—Å—Ç–µ–ø—Ä–∏–∏–º—Å—Ç–≤–∞.\n–£ –Ω–∞—Å –≤—ã –Ω–∞–π–¥–µ—Ç–µ —Å–∞–º—ã–µ –∞–∫—Ç—É–∞–ª—å–Ω—ã–µ –∏ –∏–Ω—Ç–µ—Ä–µ—Å–Ω—ã–µ –º–µ—Ä–æ–ø—Ä–∏—è—Ç–∏—è, –∫–æ—Ç–æ—Ä—ã–µ –ø–æ–º–æ–≥—É—Ç –≤–∞–º '
                  '—Ä–∞–∑–≤–∏–≤–∞—Ç—å—Å—è –∏ –Ω–∞—Ö–æ–¥–∏—Ç—å –Ω–æ–≤—ã—Ö –ø–∞—Ä—Ç–Ω–µ—Ä–æ–≤. –ü—Ä–∏—è—Ç–Ω–æ–≥–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è –Ω–∞—à–µ–≥–æ –±–æ—Ç–∞!')

    if phone:
        text = f"–í—ã —É—Å–ø–µ—à–Ω–æ –∑–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞–ª–∏—Å—å –ø–æ –Ω–æ–º–µ—Ä—É {phone}!\n" + start_text
    else:
        text = start_text

    return text, builder.as_markup(resize_keyboard=True)
